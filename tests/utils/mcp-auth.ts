/**
 * Verify user email via admin API (standalone function for E2E tests)
 *
 * @param baseUrl - Base URL of the application
 * @param userEmail - Email of the user to verify
 * @returns Promise that resolves when email is verified
 *
 * @example
 * await verifyUserEmail(BASE_URL, 'test@example.com');
 */
export async function verifyUserEmail(baseUrl: string, userEmail: string): Promise<void> {
  // Get cached admin session cookie
  const adminSessionCookie = await getAdminSessionCookie(baseUrl);

  // Call the internal verification function
  await verifyUserEmailViaAdmin(baseUrl, userEmail, adminSessionCookie);
}

/**
 * Create test user and optionally verify email via API
 * Pure HTTP, no browser automation
 *
 * @param baseUrl - Base URL of the application
 * @param email - User email
 * @param password - User password
 * @param name - User name
 * @param verifyEmail - Whether to verify email (default: true)
 * @returns User ID if successful
 */
export async function createTestUserViaApi(
  baseUrl: string,
  email: string,
  password: string,
  name: string,
  verifyEmail = true,
): Promise<{ userId: string }> {
  // Sign up user with legal consent (required for GDPR compliance)
  // Note: handle is auto-generated by the server from email prefix
  const signUpResponse = await fetch(`${baseUrl}/api/auth/sign-up/email`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      email,
      password,
      name,
      acceptedTermsAt: new Date().toISOString(),
      acceptedNotRussianResidentAt: new Date().toISOString(),
    }),
  });

  if (!signUpResponse.ok && signUpResponse.status !== 422) {
    throw new Error(`Sign-up failed: ${signUpResponse.status}`);
  }

  // Get cached admin session for subsequent operations
  const adminSessionCookie = await getAdminSessionCookie(baseUrl);

  // Find user ID (use search param to find by email in paginated results)
  const usersResponse = await fetch(
    `${baseUrl}/api/admin/users?search=${encodeURIComponent(email)}&limit=10`,
    {
      headers: {
        Cookie: formatSessionCookie(baseUrl, adminSessionCookie),
      },
    },
  );

  if (!usersResponse.ok) {
    throw new Error(`Failed to get users list: ${usersResponse.status}`);
  }

  const usersData = (await usersResponse.json()) as {
    data: { users: Array<{ id: string; email: string }> };
  };
  const user = usersData.data?.users?.find((u) => u.email === email);

  if (!user) {
    throw new Error(`User not found after sign-up: ${email}`);
  }

  // Verify email if requested
  if (verifyEmail) {
    await verifyUserEmailViaAdmin(baseUrl, email, adminSessionCookie);
  }

  return { userId: user.id };
}

/**
 * Block user via admin API
 * Pure HTTP, no browser automation
 *
 * @param baseUrl - Base URL of the application
 * @param userId - User ID to block
 * @param reason - Block reason (optional)
 */
export async function blockUserViaApi(
  baseUrl: string,
  userId: string,
  reason?: string,
): Promise<void> {
  const adminSessionCookie = await getAdminSessionCookie(baseUrl);

  const response = await fetch(`${baseUrl}/api/admin/users/${userId}/block`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Cookie: formatSessionCookie(baseUrl, adminSessionCookie),
    },
    body: JSON.stringify({ reason: reason || "E2E test block" }),
  });

  if (!response.ok) {
    const error = await response.text();
    throw new Error(`Failed to block user: ${response.status} ${error}`);
  }
}

